    private static List<Integer> fromEquip(int... prefixes) {
        return gather(equipItems, prefixes);
    }

    /**
     * Non-cash items equippable by ANY class (reqJob == 0, cash != 1).
     * Used for Common shops — plain stat gear any job can wear; NX cosmetics excluded.
     */
    private static List<Integer> fromEquipCommon(int... prefixes) {
        List<Integer> base = gather(equipItems, prefixes);
        List<Integer> result = new ArrayList<>();
        for (int id : base) {
            Integer rj = itemReqJob.get(id);
            if ((rj == null || rj == 0) && !cashEquipIds.contains(id)) result.add(id);
        }
        return result;
    }

    /**
     * Cash equips only (cash == 1 in WZ).
     * Used for NX shops — purely cosmetic items from the cash catalogue.
     */
    private static List<Integer> fromEquipCash(int... prefixes) {
        List<Integer> base = gather(equipItems, prefixes);
        List<Integer> result = new ArrayList<>();
        for (int id : base) {
            if (cashEquipIds.contains(id)) result.add(id);
        }
        return result;
    }

    /**
     * Items that REQUIRE a specific job class (reqJob & classBit != 0).
     * Used for class-specific shops — only items exclusive to that class appear.
     * reqJob bitmask: 1=Warrior, 2=Magician, 4=Archer, 8=Thief, 16=Pirate
     */
    private static List<Integer> fromEquipForJob(int classBit, int... prefixes) {
        List<Integer> base = gather(equipItems, prefixes);
        List<Integer> result = new ArrayList<>();
        for (int id : base) {
            Integer rj = itemReqJob.get(id);
            if (rj != null && rj != 0 && (rj & classBit) != 0) result.add(id);
        }
        return result;
    }

    private static List<Integer> fromUse(int... prefixes) {
        return gather(useItems, prefixes);
    }

    private static List<Integer> fromSetup(int... prefixes) {
        return gather(setupItems, prefixes);
    }

    private static List<Integer> fromCash(int... prefixes) {
        return gather(cashItems, prefixes);
    }

    private static List<Integer> fromTaming(int... prefixes) {
        return gather(tamingItems, prefixes);
    }

    private static List<Integer> gather(Map<Integer, List<Integer>> source, int... prefixes) {
        List<Integer> result = new ArrayList<>();
        for (int prefix : prefixes) {
            List<Integer> items = source.get(prefix);
            if (items != null) result.addAll(items);
        }
        return result;
    }

    /** Filters Weapon.txt items whose name contains "Maple". */
    private static List<Integer> mapleWeapons() {
        List<Integer> result = new ArrayList<>();
        for (Map.Entry<Integer, String> entry : weaponNames.entrySet()) {
            if (entry.getValue().contains("Maple")) {
                result.add(entry.getKey());
            }
        }
        return result;
    }

