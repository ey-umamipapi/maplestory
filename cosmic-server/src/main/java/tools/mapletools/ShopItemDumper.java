    /**
     * Scans Character.wz and Item.wz to collect every item ID present in the local WZ files.
     * Only these IDs will appear in the generated SQL — anything else would crash the v83 client.
     */
    private static void loadWzValidIds() throws IOException {
        System.out.println("[ShopItemDumper] Scanning WZ files for valid item IDs...");

        // Character.wz — equip items. Each equip is a file named XXXXXXXX.img.xml
        // Skip files directly in the Character.wz root (those aren't items)
        File charWz = new File(WZFiles.CHARACTER.getFilePath());
        if (charWz.isDirectory()) {
            for (File subDir : charWz.listFiles()) {
                if (subDir.isDirectory()) {
                    scanWzDirectory(subDir);
                }
            }
        } else {
            System.out.println("[ShopItemDumper] WARN: Character.wz not found at " + charWz.getAbsolutePath());
        }

        // Item.wz — USE/SETUP/ETC items. Each item is also a file named XXXXXXXX.img.xml
        File itemWz = new File(WZFiles.ITEM.getFilePath());
        if (itemWz.isDirectory()) {
            scanWzDirectory(itemWz);
        } else {
            System.out.println("[ShopItemDumper] WARN: Item.wz not found at " + itemWz.getAbsolutePath());
        }

        System.out.println("[ShopItemDumper] WZ scan complete — " + wzValidIds.size() + " valid item IDs found.");
    }

    private static void scanWzDirectory(File dir) {
        File[] files = dir.listFiles();
        if (files == null) return;
        for (File f : files) {
            if (f.isDirectory()) {
                scanWzDirectory(f);
            } else if (f.getName().endsWith(".img.xml")) {
                int id = parseWzFilename(f.getName());
                if (id > 0) {
                    wzValidIds.add(id);
                    // Parse equip metadata for class/level/cash filtering
                    if (id >= 1000000 && id < 2000000) {
                        parseEquipXml(f, id);
                    }
                }
            }
        }
    }

    private static int parseWzFilename(String filename) {
        try {
            return Integer.parseInt(filename.replace(".img.xml", ""));
        } catch (NumberFormatException e) {
            return -1;
        }
    }

    /**
     * Single-pass read of a Character.wz equip XML.
     * Extracts reqJob, reqLevel, and cash flag into the shared maps/sets.
     */
    private static void parseEquipXml(File xmlFile, int id) {
        int reqJob = 0, reqLevel = 0;
        boolean isCash = false;
        try (BufferedReader br = new BufferedReader(
                new InputStreamReader(new FileInputStream(xmlFile), StandardCharsets.UTF_8))) {
            String line;
            int lineCount = 0;
            while ((line = br.readLine()) != null && lineCount < 300) {
                lineCount++;
                if (line.contains("reqJob"))          reqJob   = extractXmlInt(line);
                else if (line.contains("reqLevel"))   reqLevel = extractXmlInt(line);
                else if (line.contains("name=\"cash\"")) isCash = (extractXmlInt(line) == 1);
            }
        } catch (Exception ignored) {}
        itemReqJob.put(id, reqJob);
        itemReqLevel.put(id, reqLevel);
        if (isCash) cashEquipIds.add(id);
    }

    /** Extracts the integer from a WZ XML attribute: value="N" */
    private static int extractXmlInt(String line) {
        try {
            int vi = line.indexOf("value=\"") + 7;
            int ve = line.indexOf("\"", vi);
            if (vi > 6 && ve > vi) return Integer.parseInt(line.substring(vi, ve));
        } catch (Exception ignored) {}
        return 0;
    }

