    /**
     * Loads all Equip handbook files into equipItems.
     * Also populates weaponNames (itemId → name) for Maple-weapon filtering.
     */
    private static void loadEquipFiles() throws IOException {
        String[] equipFiles = {
            "Cap", "Coat", "Pants", "Longcoat", "Glove", "Shield",
            "Shoes", "Cape", "Accessory", "Ring", "PetEquip", "Weapon"
        };
        for (String f : equipFiles) {
            String path = HB + "/Equip/" + f + ".txt";
            File file = new File(path);
            if (!file.exists()) {
                System.out.println("[ShopItemDumper] Skipping missing file: " + path);
                continue;
            }
            boolean isWeapon = f.equals("Weapon");
            try (BufferedReader br = new BufferedReader(
                    new InputStreamReader(new FileInputStream(file), StandardCharsets.UTF_8))) {
                String line;
                while ((line = br.readLine()) != null) {
                    line = line.trim();
                    if (line.isEmpty()) continue;
                    int[] parsed = parseLine(line);
                    if (parsed == null) continue;
                    int itemId = parsed[0];
                    int prefix = itemId / 10000;
                    equipItems.computeIfAbsent(prefix, k -> new ArrayList<>()).add(itemId);
                    if (isWeapon) {
                        // Store name for Maple-weapon filtering
                        int dash1 = line.indexOf(" - ");
                        if (dash1 >= 0) {
                            int dash2 = line.indexOf(" - ", dash1 + 3);
                            String name = dash2 >= 0
                                ? line.substring(dash1 + 3, dash2).trim()
                                : line.substring(dash1 + 3).trim();
                            weaponNames.put(itemId, name);
                        }
                    }
                }
            }
            System.out.println("[ShopItemDumper] Loaded: " + path);
        }
    }

    /**
     * Generic loader — reads a handbook file and adds items to the target map.
     * Format per line: "itemId - name - description"
     */
    private static void loadFile(String path, Map<Integer, List<Integer>> target) throws IOException {
        File file = new File(path);
        if (!file.exists()) {
            System.out.println("[ShopItemDumper] Skipping missing file: " + path);
            return;
        }
        try (BufferedReader br = new BufferedReader(
                new InputStreamReader(new FileInputStream(file), StandardCharsets.UTF_8))) {
            String line;
            while ((line = br.readLine()) != null) {
                line = line.trim();
                if (line.isEmpty()) continue;
                int[] parsed = parseLine(line);
                if (parsed == null) continue;
                int prefix = parsed[0] / 10000;
                target.computeIfAbsent(prefix, k -> new ArrayList<>()).add(parsed[0]);
            }
        }
        System.out.println("[ShopItemDumper] Loaded: " + path);
    }

    /**
     * Parses "itemId - name - ..." and returns [itemId] or null on failure.
     */
    private static int[] parseLine(String line) {
        int dash = line.indexOf(" - ");
        if (dash < 0) return null;
        try {
            int itemId = Integer.parseInt(line.substring(0, dash).trim());
            return new int[]{itemId};
        } catch (NumberFormatException e) {
            return null;
        }
    }

